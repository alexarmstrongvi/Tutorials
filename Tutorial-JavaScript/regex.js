// match and matchAll
var res = str.match(/[A-D]/)
assert(
    res.length == 1
 && res[0]     == 'A'
 && res.index  == 4
 && res.input  == str
 && res.groups == undefined
)

var res = str.match(/[A-D]/g)
assert(
    res.length == 4
 && res[0]     == 'A'
 && res[1]     == 'B'
 && res[2]     == 'C'
 && res[3]     == 'D'
)

